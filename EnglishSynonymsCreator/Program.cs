        private static void CreateTemplateSynonyms()
        {
            var client = Client.CreateFromConfig().Optimizations();

            if (client == null)
            {
                return;
            }

            foreach (KeyValuePair<string, string> entry in _synonymsEnglishList)
            {
                var key = entry.Key.Trim();
                var value = entry.Value.Trim();
                if (key.Contains(",") || key.Contains(" ") || value.Contains(" "))
                {
                    continue;
                }
                
                foreach (var v in value.Split(','))
                {
                    try
                    {
                        client.Synonyms().Add(new Synonym(key, v.Trim(), true));
                    }
                    catch (Exception ex)
                    {
                        Console.WriteLine($"Creating Synonym ({key},{v}) error: {ex.Message}");
                        continue;
                    }
                }
            }
        }
